#include "libft.h"

void	ft_last_word(char *str)
{
	int		i;

	i = ft_strlen(str);
	while (str[i] == ' ' || str[i] == '\t')
		i--;
	while (str[i] >= 33 && str[i] <= 127)
		i--;
	i++;
	while (str[i] >= 33 && str[i] <= 127)
	{
		write(1, &str[i], 1);
		i++;
	}
}
